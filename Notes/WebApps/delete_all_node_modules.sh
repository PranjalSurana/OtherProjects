# Delete temporary directories created by Angular build process.
# Run this script when your D: drive is low on free space.
# Note: this may take several minutes to run.
#
# Usage:
# 1. Copy this file to D:\
# 2. Open git bash and run the following commands:
#		bash /d/delete_all_node_modules.sh

echo "Delete all node_modules and .angular directories on your D: drive?"
echo -n "Enter y to confirm, anything else to cancel: "
read ans
if [[ $ans == 'y' ]]; then
	find /d \( -name 'node_modules' -o -name '.angular' \) -exec rm -rf {} \;
fi
